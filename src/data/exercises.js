export const exerciseMap = Object.fromEntries(exercises.map(ex => [ex.slug, ex]))

export const categories = ['chest', 'back', 'shoulders', 'arms', 'legs', 'core']

export const equipmentTypes = ['barbell', 'dumbbell', 'cable', 'machine', 'bodyweight']

export function getExerciseBySlug(slug) {
  return exerciseMap[slug] || null
}

export function getExerciseById(id) {
  return exercises.find(ex => ex.id === id) || null
}

export function filterExercises({ category, equipment, search }) {
  return exercises.filter(ex => {
    if (category && category !== 'all' && ex.category !== category) return false
    if (equipment && equipment !== 'all') {
      const eqMap = { barbell: 'barbell', dumbbell: 'dumbbells', cable: 'cable machine', machine: 'machine', bodyweight: '' }
      if (equipment === 'bodyweight') {
        if (ex.equipment.length > 0) return false
      } else {
        if (!ex.equipment.some(e => e.includes(eqMap[equipment] || equipment))) return false
      }
    }
    if (search) {
      const q = search.toLowerCase()
      if (!ex.name.toLowerCase().includes(q) && !ex.primary_muscles.join(' ').toLowerCase().includes(q)) return false
    }
    return true
  })
}
