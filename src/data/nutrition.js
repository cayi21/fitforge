export function getTipsForGoal(goal) {
  const goalMap = {
    'build_muscle': 'muscle_gain',
    'lose_fat': 'fat_loss',
    'recomposition': 'recomposition',
    'strength': 'general',
    'general_fitness': 'general',
  }
  const mapped = goalMap[goal] || 'general'
  return nutritionTips.filter(tip => tip.goal.includes(mapped) || tip.goal.includes('general'))
}

export function getDailyTip(goal, date = new Date()) {
  const tips = getTipsForGoal(goal)
  const dayIndex = Math.floor(date.getTime() / 86400000)
  return tips[dayIndex % tips.length]
}
